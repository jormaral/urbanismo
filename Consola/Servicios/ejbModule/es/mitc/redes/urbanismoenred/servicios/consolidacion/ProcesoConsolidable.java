package es.mitc.redes.urbanismoenred.servicios.consolidacion;

import java.util.Date;

/**
 * Clase contenedora de la información requerida para el proceso de consolidación.
 * 
 * @author Arnaiz Consultores
 *
 */
public class ProcesoConsolidable {
	private int ambito;
	private String codigoTramite;
	private Date fecha;
	private int iteracion;
	private String nombrePlan;
	private String nombreTramite;

	private int proceso;

	private Integer tipoInstrumento;
	private int tipoTramite;

	/**
	 * @param proceso
	 * @param codigoTramite
	 * @param fecha
	 * @param iteracion
	 */
	public ProcesoConsolidable(int proceso, 
			String codigoTramite, 
			int tipoTramite,
			Date fecha,
			int iteracion, 
			int ambito,
			Integer tipoInstrumento, 
			String nombrePlan, 
			String nombreTramite) {
		super();
		this.proceso = proceso;
		this.codigoTramite = codigoTramite;
		this.fecha = fecha;
		this.iteracion = iteracion;
		this.tipoTramite = tipoTramite;
		this.ambito = ambito;
		this.tipoInstrumento = tipoInstrumento;
		this.nombrePlan = nombrePlan;
		this.nombreTramite = nombreTramite;
	}

	/**
	 * @return the ambito
	 */
	public int getAmbito() {
		return ambito;
	}
	/**
	 * @return the codigoTramite
	 */
	public String getCodigoTramite() {
		return codigoTramite;
	}
	/**
	 * @return the fecha
	 */
	public Date getFecha() {
		return fecha;
	}
	
	/**
	 * @return the iteracion
	 */
	public int getIteracion() {
		return iteracion;
	}
	/**
	 * @return the nombrePlan
	 */
	public String getNombrePlan() {
		return nombrePlan;
	}
	/**
	 * @return the nombreTramite
	 */
	public String getNombreTramite() {
		return nombreTramite;
	}
	/**
	 * @return the proceso
	 */
	public int getProceso() {
		return proceso;
	}

	/**
	 * @return the tipoInstrumento
	 */
	public Integer getTipoInstrumento() {
		return tipoInstrumento;
	}

	/**
	 * @return the tipoTramite
	 */
	public int getTipoTramite() {
		return tipoTramite;
	}
}
