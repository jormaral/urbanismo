package es.mitc.redes.urbanismoenred.servicios.dal;

import javax.ejb.ApplicationException;

/**
 * 
 * Expecición generada en la capa de aceso a datos.
 * 
 * @author jgarzon
 *
 */
@ApplicationException(rollback = true)
public class ExcepcionPersistencia extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6140706994920013446L;

	/**
	 * 
	 */
	public ExcepcionPersistencia() {
		super();
	}

	/**
	 * @param message
	 */
	public ExcepcionPersistencia(String message) {
		super(message);
	}

	/**
	 * @param cause
	 */
	public ExcepcionPersistencia(Throwable cause) {
		super(cause);
	}

	/**
	 * @param message
	 * @param cause
	 */
	public ExcepcionPersistencia(String message, Throwable cause) {
		super(message, cause);
	}

}
