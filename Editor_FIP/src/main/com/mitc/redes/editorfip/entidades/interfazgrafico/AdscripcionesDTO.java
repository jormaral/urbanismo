package com.mitc.redes.editorfip.entidades.interfazgrafico;

import java.io.Serializable;

public class AdscripcionesDTO implements Serializable {
	
	private int idRelacion=0;
	private int idEntidadOrigen=0;
	private String nombreEntidadOrigen;
	private int idEntidadDestino=0;
	private String nombreEntidadDestino;
	private double cuantia=0;
	private int idDeterminacionUnidad=0;
	private String nombreDeterminacionUnidad;
	private String nombreTramiteDeterminacionUnidad;
	private int idDeterminacionTipo=0;
	private String nombreDeterminacionTipo;
	private String nombreTramiteDeterminacionTipo;
	private String texto;
	
	
	
	public AdscripcionesDTO() {
		super();
		
	}
	
		

	public AdscripcionesDTO(int idRelacion, int idEntidadOrigen,
			String nombreEntidadOrigen, int idEntidadDestino,
			String nombreEntidadDestino, double cuantia,
			int idDeterminacionUnidad, String nombreDeterminacionUnidad,
			String nombreTramiteDeterminacionUnidad, int idDeterminacionTipo,
			String nombreDeterminacionTipo,
			String nombreTramiteDeterminacionTipo, String texto) {
		super();
		this.idRelacion = idRelacion;
		this.idEntidadOrigen = idEntidadOrigen;
		this.nombreEntidadOrigen = nombreEntidadOrigen;
		this.idEntidadDestino = idEntidadDestino;
		this.nombreEntidadDestino = nombreEntidadDestino;
		this.cuantia = cuantia;
		this.idDeterminacionUnidad = idDeterminacionUnidad;
		this.nombreDeterminacionUnidad = nombreDeterminacionUnidad;
		this.nombreTramiteDeterminacionUnidad = nombreTramiteDeterminacionUnidad;
		this.idDeterminacionTipo = idDeterminacionTipo;
		this.nombreDeterminacionTipo = nombreDeterminacionTipo;
		this.nombreTramiteDeterminacionTipo = nombreTramiteDeterminacionTipo;
		this.texto = texto;
	}




	public String getNombreTramiteDeterminacionUnidad() {
		return nombreTramiteDeterminacionUnidad;
	}



	public void setNombreTramiteDeterminacionUnidad(
			String nombreTramiteDeterminacionUnidad) {
		this.nombreTramiteDeterminacionUnidad = nombreTramiteDeterminacionUnidad;
	}



	public String getNombreTramiteDeterminacionTipo() {
		return nombreTramiteDeterminacionTipo;
	}



	public void setNombreTramiteDeterminacionTipo(
			String nombreTramiteDeterminacionTipo) {
		this.nombreTramiteDeterminacionTipo = nombreTramiteDeterminacionTipo;
	}



	public int getIdRelacion() {
		return idRelacion;
	}

	public void setIdRelacion(int idRelacion) {
		this.idRelacion = idRelacion;
	}

	public int getIdEntidadOrigen() {
		return idEntidadOrigen;
	}

	public void setIdEntidadOrigen(int idEntidadOrigen) {
		this.idEntidadOrigen = idEntidadOrigen;
	}

	public String getNombreEntidadOrigen() {
		return nombreEntidadOrigen;
	}

	public void setNombreEntidadOrigen(String nombreEntidadOrigen) {
		this.nombreEntidadOrigen = nombreEntidadOrigen;
	}

	public int getIdEntidadDestino() {
		return idEntidadDestino;
	}

	public void setIdEntidadDestino(int idEntidadDestino) {
		this.idEntidadDestino = idEntidadDestino;
	}

	public String getNombreEntidadDestino() {
		return nombreEntidadDestino;
	}

	public void setNombreEntidadDestino(String nombreEntidadDestino) {
		this.nombreEntidadDestino = nombreEntidadDestino;
	}

	public double getCuantia() {
		return cuantia;
	}

	public void setCuantia(double cuantia) {
		this.cuantia = cuantia;
	}

	public int getIdDeterminacionUnidad() {
		return idDeterminacionUnidad;
	}

	public void setIdDeterminacionUnidad(int idDeterminacionUnidad) {
		this.idDeterminacionUnidad = idDeterminacionUnidad;
	}

	public String getNombreDeterminacionUnidad() {
		return nombreDeterminacionUnidad;
	}

	public void setNombreDeterminacionUnidad(String nombreDeterminacionUnidad) {
		this.nombreDeterminacionUnidad = nombreDeterminacionUnidad;
	}

	public int getIdDeterminacionTipo() {
		return idDeterminacionTipo;
	}

	public void setIdDeterminacionTipo(int idDeterminacionTipo) {
		this.idDeterminacionTipo = idDeterminacionTipo;
	}

	public String getNombreDeterminacionTipo() {
		return nombreDeterminacionTipo;
	}

	public void setNombreDeterminacionTipo(String nombreDeterminacionTipo) {
		this.nombreDeterminacionTipo = nombreDeterminacionTipo;
	}

	public String getTexto() {
		return texto;
	}

	public void setTexto(String texto) {
		this.texto = texto;
	}
	
	
	


}
