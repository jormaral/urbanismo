package com.mitc.redes.editorfip.entidades.busqueda;

import java.io.Serializable;

/**
 *
 * @author Sancho
 */
public class BusquedaDeterminacionDTO implements Serializable{
	
	private String plan;
	private String nombre;
	private String articulado;
	private String apartado;
	private String tipo;
	private String tipoPlan;
	private int id;
	
	public String getPlan() {
		return plan;
	}
	public void setPlan(String plan) {
		this.plan = plan;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getArticulado() {
		return articulado;
	}
	public void setArticulado(String articulado) {
		this.articulado = articulado;
	}
	public String getApartado() {
		return apartado;
	}
	public void setApartado(String apartado) {
		this.apartado = apartado;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public String getTipoPlan() {
		return tipoPlan;
	}
	public void setTipoPlan(String tipoPlan) {
		this.tipoPlan = tipoPlan;
	}
	
}