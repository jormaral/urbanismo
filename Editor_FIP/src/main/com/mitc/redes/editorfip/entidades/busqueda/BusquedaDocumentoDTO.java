package com.mitc.redes.editorfip.entidades.busqueda;

import java.io.Serializable;

/**
 *
 * @author Sancho
 */
public class BusquedaDocumentoDTO implements Serializable{
	
	private String plan;
	private String titulo;
	private String nombreArchivo;
	private String grupo;
	private String tipo;
	
	private boolean carpeta;
	
	private String entidadAsociada;
	private String determinacionAsociada;
	private int id;
	public String getPlan() {
		return plan;
	}
	public void setPlan(String plan) {
		this.plan = plan;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public String getNombreArchivo() {
		return nombreArchivo;
	}
	public void setNombreArchivo(String nombreArchivo) {
		this.nombreArchivo = nombreArchivo;
	}
	public String getGrupo() {
		return grupo;
	}
	public void setGrupo(String grupo) {
		this.grupo = grupo;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getEntidadAsociada() {
		return entidadAsociada;
	}
	public void setEntidadAsociada(String entidadAsociada) {
		this.entidadAsociada = entidadAsociada;
	}
	public String getDeterminacionAsociada() {
		return determinacionAsociada;
	}
	public void setDeterminacionAsociada(String determinacionAsociada) {
		this.determinacionAsociada = determinacionAsociada;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public boolean isCarpeta() {
		return carpeta;
	}
	public void setCarpeta(boolean carpeta) {
		this.carpeta = carpeta;
	}
	
	
	
}