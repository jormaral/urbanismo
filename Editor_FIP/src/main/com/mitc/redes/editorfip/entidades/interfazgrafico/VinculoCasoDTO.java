package com.mitc.redes.editorfip.entidades.interfazgrafico;

public class VinculoCasoDTO {

	private int idVinculoCaso;
	private String nombreTramite;
	private String nombreCaso;
	
	
	public VinculoCasoDTO() {
		super();
		
	}


	
	public VinculoCasoDTO(int idVinculoCaso, String nombreTramite,
			String nombreCaso) {
		super();
		this.idVinculoCaso = idVinculoCaso;
		this.nombreTramite = nombreTramite;
		this.nombreCaso = nombreCaso;
	}


	public int getIdVinculoCaso() {
		return idVinculoCaso;
	}



	public void setIdVinculoCaso(int idVinculoCaso) {
		this.idVinculoCaso = idVinculoCaso;
	}



	public String getNombreTramite() {
		return nombreTramite;
	}


	public void setNombreTramite(String nombreTramite) {
		this.nombreTramite = nombreTramite;
	}


	public String getNombreCaso() {
		return nombreCaso;
	}


	public void setNombreCaso(String nombreCaso) {
		this.nombreCaso = nombreCaso;
	}
	
	
	

}
