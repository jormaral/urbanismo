def cogerDatosBD(bdMaquina='', bdNombre='', bdUsuario='', bdContrasena=''):

		# Obtener todos los datos
		if (bdMaquina==''): bdMaquina = raw_input("Dirección IP del servidor de base de datos (ej: 127.0.0.1): ");
		if (bdNombre==''): bdNombre = raw_input("Nombre de la base de datos (ej: fipeditor_v060): ");
		if (bdUsuario==''): bdUsuario = raw_input("Usuario (ej:postgres): ");
		if (bdContrasena==''): bdContrasena = raw_input("Contraseña (ej:UrBr09): ");

		return [bdMaquina, bdNombre, bdUsuario, bdContrasena]


